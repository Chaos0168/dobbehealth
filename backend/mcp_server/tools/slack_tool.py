def _format_for_slack(text: str) -> str:
    """Convert plain text report to Slack markdown format"""
    lines = text.split("\n")
    formatted = []
    for line in lines:
        if line.startswith("  •"):
            formatted.append(line)  # bullet stays as-is
        elif line.startswith("📊") or line.startswith("📋") or line.startswith("🔍"):
            formatted.append(f"*{line}*")  # bold headers
        elif "─" in line:
            formatted.append("")  # skip divider lines, Slack has its own
        else:
            formatted.append(line)
    return "\n".join(formatted)
